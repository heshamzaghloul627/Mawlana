type Mark = { type: string };
const B: Mark = { type: "bold" };
const I: Mark = { type: "italic" };

const t = (str: string, marks?: Mark[]) => {
  const node: any = { type: "text", text: str };
  if (marks && marks.length) node.marks = marks;
  return node;
};

const doc = (...content: any[]) => ({ type: "doc", content });

const h = (level: number, str: string) => ({
  type: "heading",
  attrs: { level },
  content: [t(str)],
});

const p = (...content: any[]) => ({ type: "paragraph", content });

const verse = (text: string, surah: string, ayah: string) => ({
  type: "quranVerse",
  attrs: { surah, ayah },
  content: [t(text)],
});

const poetry = (...lines: string[]) => ({
  type: "poetryBlock",
  content: lines.map((line) => ({
    type: "poetryLine",
    content: [t(line)],
  })),
});

const quote = (...paragraphs: any[]) => ({
  type: "blockquote",
  content: paragraphs,
});

const list = (...items: any[][]) => ({
  type: "bulletList",
  content: items.map((content) => ({
    type: "listItem",
    content: [{ type: "paragraph", content }],
  })),
});

