async function seed() {
  console.log("🌱 Seeding demo articles for 10 new categories...\n");
  const articlesRef = collection(db, "articles");

  for (const article of articles) {
    const slug = makeSlug(article.title_ar);
    const data = {
      slug_ar: slug,
      slug_en: slug,
      title_ar: article.title_ar,
      title_en: null,
      excerpt_ar: article.excerpt_ar,
      excerpt_en: null,
      content_ar: makeTiptapContent(article.body_paragraphs),
      content_en: null,
      category: article.category,
      status: "published",
      featured: false,
      version_ar: 1,
      version_en: 0,
      author: "الشيخ أحمد الرفاعي",
      coverImage: null,
      seo_meta_ar: {
        title: article.title_ar,
        description: article.excerpt_ar,
        keywords: [article.category],
      },
      seo_meta_en: null,
      created_at: Timestamp.now(),
      updated_at: Timestamp.now(),
    };

    try {
      const docRef = await addDoc(articlesRef, data);
      console.log(`  ✅ [${article.category}] ${article.title_ar} → ${docRef.id}`);
    } catch (err) {
      console.error(`  ❌ [${article.category}] ${article.title_ar}:`, err);
    }
  }

  console.log("\n✨ Done! Seeded", articles.length, "articles across 10 categories.");
}

seed().catch(console.error);
